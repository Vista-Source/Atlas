#pragma once

// Integer operations

// [CSharpAPI]
int Add(int a, int b)
{
    return a + b;
}

// [CSharpAPI]
int Subtract(int a, int b)
{
    int output = a - b;
    return output;
}

// [CSharpAPI]
int Multiply(int a, int b)
{
    return a * b;
}

// Floating-point operations

// [CSharpAPI]
float Add(float a, float b)
{
    return a + b;
}

// [CSharpAPI]
float Subtract(float a, float b)
{
    return a - b;
}

// [CSharpAPI]
float Multiply(float a, float b)
{
    return a * b;
}
